<template>
    <div>
      <div class="message-post-details">
        <div class="message-post-avatar">
            <img :src="image" alt="avatar">
        </div>
        <h3 class="message-post-name heading-2">{{ name }}</h3>
        <h3 class="message-post-time copy-muted-2 font-sm font-thin" :class="{highlighted: hightlighted}">{{ momentDate }}</h3>
      </div>
      <p class="message-post-body">{{ message }}</p>
    </div>
</template>

<script>
    import moment from 'moment'
    import _ from 'lodash'
    export default {
        props: ['name', 'day', 'time', 'header', 'message', 'image', 'id', 'userId', 'timezone', 'hightlighted'],
        name: 'MessagingPost',
        data() {
            return {  }
        },
        computed: {
            momentDate() {
                moment.tz.add(this.$props.timezone);
                return `${moment(this.$props.day).format("M/D/YYYY")} ${moment(this.$props.time).format("h:mm a")} ${moment.tz(moment.tz.guess()).format('z')}`
            }
        },
        mounted() {
            if (this.$root.$data.global.user.id == this.$props.userId) {
                axios.put(`${this.$root.$data.apiUrl}/messages/${this.$props.id}/read`)
                    .then(response => {
                        this.$root.$data.global.unreadMessages = this.$root.$data.global.unreadMessages.filter(e => e.id !== this.$props.id)
                    })
            }
        }
    }
</script>
